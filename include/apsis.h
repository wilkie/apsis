/*  Apsis -- OpenSource Multipurpose Game Engine  */

/*
 *  This is the main namespace for Apsis. All public code is available through
 *  this namespace.
 */
namespace Apsis {
  /*
   *  Contains various classes that are responsible for managing subsystems.
   */
  namespace Engine {
  }

  /*
   *  Contains classes used to describe the environment that interacting
   *  objects in the system live.
   */
  namespace World {
  }

  /*
   *  Contains routines that describe how objects in the world interact with
   *  each other and the scene itself.
   */
  namespace Rules {
  }

  /*
   *  Contains low-level classes that help synchronize and time events occuring
   *  throughout the system.
   */
  namespace Sync {
  }

  /*
   *  Contains configuration classes that describe what types of features and
   *  what properties of those features are used.
   */
  namespace Settings {
  }

  /*
   *  Contains collections of systems objects as they are loaded into the
   *  system and assigns each a unique identifier.
   */
  namespace Registry {
  }

  /*
   *  Contains low-level graphics primitive used through the system to build
   *  more complicated objects.
   */
  namespace Primitive {
  }

  /*
   *  Contains the classes related to describing and dealing with basic
   *  geometry.
   */
  namespace Geometry {
  }

  /*
   *  Contains classes related to the human/user interface, such as widgets
   *  (buttons, check boxes, etc) and behaviors related to handling input.
   */
  namespace Interface {
  }

  /*
   *  Contains classes related to three-dimensional objects and rendering.
   */
  namespace Model {
  }

  /*
   *  Contains classes related to two-dimensional objects and rendering.
   */
  namespace Sprite {
  }

  /*
   *  Contains classes related to the playing and manipulation of audio.
   */
  namespace Audio {
  }

  /*
   *  Contains classes related to low-level support for various devices
   *  and capabilities.
   */
  namespace Backend {
  }
}
